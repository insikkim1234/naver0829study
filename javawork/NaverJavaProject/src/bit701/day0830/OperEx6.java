package bit701.day0830;

public class OperEx6 {

	public static void main(String[] args) {
		//산술 연산자 예시
		
		int su1=7,su2=4;
		System.out.println(su1+su2);
		System.out.println(su1-su2);
		System.out.println(su1*su2);
		System.out.println(su1/su2);
		System.out.println((double)su1/su2);
		//최소 한개 이상만 더블로 바꿔주면1.75
		System.out.println(su1%su2);

	}

}
